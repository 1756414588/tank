package com.game.domain.s;

public class StaticServerPartyWining {
  private int id;
  private int time;
  private int score;
  private int shutdown;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public int getTime() {
    return time;
  }

  public void setTime(int time) {
    this.time = time;
  }

  public int getScore() {
    return score;
  }

  public void setScore(int score) {
    this.score = score;
  }

  public int getShutdown() {
    return shutdown;
  }

  public void setShutdown(int shutdown) {
    this.shutdown = shutdown;
  }
}
