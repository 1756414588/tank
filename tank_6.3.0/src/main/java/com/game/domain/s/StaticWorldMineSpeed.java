package com.game.domain.s;

public class StaticWorldMineSpeed {


    private int id;
    private int a;
    private int b;
    private int capBegin;
    private int capEnd;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getCapBegin() {
        return capBegin;
    }

    public void setCapBegin(int capBegin) {
        this.capBegin = capBegin;
    }

    public int getCapEnd() {
        return capEnd;
    }

    public void setCapEnd(int capEnd) {
        this.capEnd = capEnd;
    }
}
