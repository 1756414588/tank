package com.game.domain.s.friend;

public class StaticFriend {

    private int id;
    private int prop;
    private int friendAdd;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getProp() {
        return prop;
    }

    public void setProp(int prop) {
        this.prop = prop;
    }

    public int getFriendAdd() {
        return friendAdd;
    }

    public void setFriendAdd(int friendAdd) {
        this.friendAdd = friendAdd;
    }
}
