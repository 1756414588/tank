package com.game.domain.s;

import java.util.List;

public class StaticKingActRatio {

    private int id;
    private int type;
    private List<Integer> ratio;
    private int date;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public List<Integer> getRatio() {
        return ratio;
    }

    public void setRatio(List<Integer> ratio) {
        this.ratio = ratio;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }


}
