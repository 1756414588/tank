package com.game.domain.s;

public class StaticWorldMine {

    private int lv;
    private long exp;
    private long worldExp;
    private int decline;

    public int getLv() {
        return lv;
    }

    public void setLv(int lv) {
        this.lv = lv;
    }

    public long getExp() {
        return exp;
    }

    public void setExp(long exp) {
        this.exp = exp;
    }

    public long getWorldExp() {
        return worldExp;
    }

    public void setWorldExp(long worldExp) {
        this.worldExp = worldExp;
    }

    public int getDecline() {
        return decline;
    }

    public void setDecline(int decline) {
        this.decline = decline;
    }
}
