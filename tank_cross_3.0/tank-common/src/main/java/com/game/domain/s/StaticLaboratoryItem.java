public class StaticLaboratoryItem {

  private int kid;
  private int id;
  private String name;
  private int ifPieces;
  private int maxProduceTime;
  private int amountPmm;
  private String description;
  private String picture;

  public int getKid() {
    return kid;
  }

  public void setKid(int kid) {
    this.kid = kid;
  }

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getIfPieces() {
    return ifPieces;
  }

  public void setIfPieces(int ifPieces) {
    this.ifPieces = ifPieces;
  }

  public int getMaxProduceTime() {
    return maxProduceTime;
  }

  public void setMaxProduceTime(int maxProduceTime) {
    this.maxProduceTime = maxProduceTime;
  }

  public int getAmountPmm() {
    return amountPmm;
  }

  public void setAmountPmm(int amountPmm) {
    this.amountPmm = amountPmm;
  }

  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }

  public String getPicture() {
    return picture;
  }

  public void setPicture(String picture) {
    this.picture = picture;
  }
}
